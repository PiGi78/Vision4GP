namespace Vision4GP.Core.FileSystem
{

    /// <summary>
    /// Start mode for vision file
    /// </summary>
    public enum FileStartMode
    {
        /// <summary>
        /// Key is equal
        /// </summary>
        Equal,
        /// <summary>
        /// Key is greater
        /// </summary>
        Greater,
        /// <summary>
        /// Key is greater or equal
        /// </summary>
        GreaterOrEqual,
        /// <summary>
        /// Key is less
        /// </summary>
        Less,
        /// <summary>
        /// Key is less or equal
        /// </summary>
        LessOrEqual
    }

}